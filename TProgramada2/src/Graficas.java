    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ChartPanel panel;
        JFreeChart chart=null;
        int especial,mayor,embarazada,regular,corporativo;
        //especial,mayor,embarazada,regular,corporativo
       // ComboDia.getSelectedItem()
        if(ComboDia.getSelectedIndex()==0){
            System.out.println("Funca");
        especial=this.actual.getEspecial();
        mayor=this.actual.getAmayor();
        embarazada=this.actual.getEmbrazadas();
        corporativo=this.actual.getCorporativos();
        regular=this.actual.getRegulares();
        }else{
        if((int)this.horaIni.getValue()>=(int)this.horaFin.getValue()){
            JOptionPane.showMessageDialog(rootPane,"Error al introducir hora de reporte inicial\n hora inicial mayor o igual a la final" );
            return;
        }
        especial=this.actual.horavshoraES((int)this.horaIni.getValue(), (int)this.horaFin.getValue());
        mayor=this.actual.horavshoraAM((int)this.horaIni.getValue(), (int)this.horaFin.getValue());
        embarazada=this.actual.horavshoraEM((int)this.horaIni.getValue(), (int)this.horaFin.getValue());
        corporativo=this.actual.horavshoraCO((int)this.horaIni.getValue(), (int)this.horaFin.getValue());
        regular=this.actual.horavshoraRE((int)this.horaIni.getValue(), (int)this.horaFin.getValue());
        }
        
        
           if(b.isSelected()){
               //Grafico de Barras
               DefaultCategoryDataset data = new DefaultCategoryDataset();
               
               String categoria1 ="Especiales";
               String categoria2 ="A.Mayor";
               String categoria3 ="Embarazadas";
               String categoria4 ="Corporativo";
               String categoria5 ="Regulares";
               
               data.addValue(especial,categoria1,categoria1);
               data.addValue(mayor,categoria2,categoria2);
               data.addValue(embarazada,categoria3,categoria3);
               data.addValue(corporativo,categoria4,categoria4);
               data.addValue(regular,categoria5,categoria5);
               
               
               chart = ChartFactory.createBarChart("Grafico de Barras","Tipo de Cliente","Cantidad",
                       data,
                       PlotOrientation.VERTICAL,
                       true,
                       true,
                       true);
               CategoryPlot plot = (CategoryPlot) chart.getPlot();
               plot.setDomainGridlinesVisible(true);
               
           }else{
               DefaultPieDataset data = new DefaultPieDataset();
            data.setValue("Especial",especial);
            data.setValue("A.Mayor",mayor);
            data.setValue("Embarazadas",embarazada);
            data.setValue("Corporativas",corporativo);
            data.setValue("Regulares",regular);
            chart = ChartFactory.createPieChart("Grafico de Pastel",data, true,true,true);
            // BufferedImage imagen = chart.createBufferedImage(300, 300, BufferedImage.TYPE_INT_RGB, null);
              //  File JPG=new File("grafico.jpg"); 
               // ImageIO.write(imagen,"jpg",JPG);
           
       }
        panel= new ChartPanel(chart);
        panel.setBounds(5,10,400,350);
        if (GuarImage.isSelected()){
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG", "jpg");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showSaveDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                BufferedImage imagen = chart.createBufferedImage(300, 300, BufferedImage.TYPE_INT_RGB, null);
                File JPG=new File(chooser.getSelectedFile().getAbsolutePath()+".jpg");
                ImageIO.write(imagen,"jpg",JPG);
            } catch (IOException ex) {
                Logger.getLogger(Graficas.class.getName()).log(Level.SEVERE, null, ex);
            }}
    }
        
        
            if(b.isSelected()){
                //Systessm.out.println("Entra");
                Barras.removeAll();
                Barras.add(panel);
                Barras.repaint();
                
            }else{
                Pastel.removeAll();
                Pastel.add(panel);
                Pastel.repaint();
        }
        
        //new Graficas().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

